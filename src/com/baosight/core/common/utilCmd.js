/**  
* @description 判断数组中是否有ele元素
* @param arr 数组
* @param ele 元素
* @return boolean
*/
UtilCmd.prototype.contains = function(arr,ele){
	var i = arr.length;
	    while (i--) {
	        if (arr[i] === ele) {
	            return true;
	        }
	    }
	    return false;
}

